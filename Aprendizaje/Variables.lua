--- Las variables son un espacio donde podemos almacenar dato, en especial hay 3 tipos, datos numericos, datos tipo texto y datos booleanos. 
--- Los booleanos son donde solo se puede guardar un true o un false. Por ejemplo:

nombre = "Buben" --- VARIABLE TIPO TEXTO
edad = "999" --- VARIABLE NUMERICA
suscriptores = 16 --- VARIABLE NUMERICA

--- Ahora este es un ejemplo de unas pocas lineas de codigo para decir que Buben tiene 999 años y 16 suscriptores.

print("Se llama", nombre, "su edad es", edad, "años, y tiene", suscriptores, "suscriptores")

--- Este texto aparecera en la consola como:  "Se llama Buben su edad es 999 años, y tiene 16 suscriptores"

